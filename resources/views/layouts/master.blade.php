    <script>
        // Script untuk toggle sidebar dan menambahkan menu
        const menuBtn = document.getElementById('menu-btn');
        const sidebar = document.getElementById('sidebar');
        const sidebarMenu = document.getElementById('sidebar-menu');

        // Menu baru
        const newMenuItem = document.createElement('li');
        newMenuItem.innerHTML =
            `
        <form action="{{ route('logout') }}" method="post">
            @csrf
            <button type="submit"
                class="block rounded-lg px-4 py-3 text-sm md:hidden lg:hidden xl:hidden font-medium text-white hover:bg-gray-100 hover:text-gray-700"><i
                    class="fas fa-sign-out-alt mr-2"></i> Keluar</button>
        </form>
        `

        menuBtn.addEventListener('click', () => {
            sidebar.classList.toggle('hidden-sidebar');

            if (sidebar.classList.contains('hidden-sidebar')) {
                sidebarMenu.removeChild(newMenuItem);
            } else {
                sidebarMenu.appendChild(newMenuItem);
            }
        });
    </script>
